// ─── Main ───────────────────────────────────────────────

async function main() {
  console.log('开始为4本书追加新章节...\n');

  for (const { bookId, newChapters } of expansions) {
    const existing = await prisma.bookSummary.findFirst({
      where: { bookId },
    });

    if (!existing) {
      console.log(`⚠ 未找到 ${bookId} 的 BookSummary，跳过`);
      continue;
    }

    const existingChapters: Chapter[] = existing.chaptersJson
      ? JSON.parse(existing.chaptersJson)
      : [];

    const mergedChapters = [...existingChapters, ...newChapters];
    const totalReadingTime = mergedChapters.reduce(
      (sum, ch) => sum + ch.readingTimeMin,
      0,
    );

    await prisma.bookSummary.update({
      where: { id: existing.id },
      data: {
        chaptersJson: JSON.stringify(mergedChapters),
        readingTime: totalReadingTime,
      },
    });

    console.log(
      `✓ ${bookId}: ${existingChapters.length} 章 → ${mergedChapters.length} 章 (阅读时间 ${totalReadingTime} 分钟)`,
    );
  }

  console.log('\n全部完成！');
}

main()
  .catch((err) => {
    console.error('执行失败:', err);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
