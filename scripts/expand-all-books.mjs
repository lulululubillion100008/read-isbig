// Generic expansion function for books with themed data
// ══════════════════════════════════════════════════════

function buildChaptersFromData(bookTitle, data) {
  const numLabels = ['一','二','三','四','五','六','七','八','九','十','十一','十二']
  return data.themes.map((theme, i) => ({
    id: `ch-${data.slug}-${i + 1}`,
    title: `第${numLabels[i]}章 · ${theme.title}`,
    readingTimeMin: 3,
    blocks: [
      h(theme.heading),
      p(theme.desc),
      q(data.quotes[i % data.quotes.length]),
      ki(theme.desc.length > 100 ? theme.desc.substring(0, theme.desc.lastIndexOf('。', 100) + 1) + '这个洞见对理解全书至关重要。' : theme.desc),
      co(`这一章揭示了《${bookTitle}》中一个关键维度。理解了这个层面，才能真正读懂这部作品的深度与力量。`),
    ],
  }))
}

// ══════════════════════════════════════════════════════
// Main execution
// ══════════════════════════════════════════════════════

const books = await query(`
  SELECT b.id, b.title, b.author, b.category, bs.chaptersJson
  FROM Book b JOIN BookSummary bs ON b.id = bs.bookId
  WHERE JSON_ARRAY_LENGTH(bs.chaptersJson) < 10
  ORDER BY b.category, b.title
`)

console.log(`需要扩展的书: ${books.rows.length} 本\n`)

let updated = 0
let errors = 0

for (const book of books.rows) {
  const data = BOOK_DATA[book.title]
  if (!data) continue

  const chapters = buildChaptersFromData(book.title, data)
  const totalTime = chapters.reduce((s, c) => s + c.readingTimeMin, 0)
  const json = JSON.stringify(chapters).replace(/'/g, "''")

  try {
    await query(`UPDATE BookSummary SET chaptersJson = '${json}', readingTime = ${totalTime} WHERE bookId = '${book.id}'`)
    updated++
    console.log(`✓ ${book.title}: → ${chapters.length}章`)
  } catch (e) {
    errors++
    console.error(`✗ ${book.title}: ${e.message?.substring(0, 80)}`)
  }
}

console.log(`\n完成: ${updated} 更新, ${errors} 错误`)
console.log(`剩余 ${books.rows.length - updated} 本需要添加 BOOK_DATA`)
