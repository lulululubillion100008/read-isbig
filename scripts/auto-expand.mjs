const books = await query(`
  SELECT b.id, b.title, b.author, b.category, bs.chaptersJson
  FROM Book b JOIN BookSummary bs ON b.id = bs.bookId
  WHERE JSON_ARRAY_LENGTH(bs.chaptersJson) < 10
  ORDER BY b.category, b.title
`)

console.log(`需要扩展: ${books.rows.length} 本\n`)

const numLabels = ['一','二','三','四','五','六','七','八','九','十']

function slug(title) {
  // Simple slug from title
  return title.replace(/[：·\s]/g, '').substring(0, 6)
}

let updated = 0, errors = 0

for (const book of books.rows) {
  const templates = CATEGORY_TEMPLATES[book.category] || CATEGORY_TEMPLATES['文学']
  const existing = JSON.parse(book.chaptersJson)
  const s = slug(book.title)

  // Keep existing chapters, add new ones to reach 10
  const newChapters = [...existing]
  const existingAngles = new Set(existing.map(c => c.title))

  for (let i = 0; newChapters.length < 10 && i < templates.length; i++) {
    const tmpl = templates[i]
    const title = `第${numLabels[newChapters.length]}章 · ${tmpl.angle}`
    if (existingAngles.has(title)) continue

    newChapters.push({
      id: `ch-${s}-${newChapters.length + 1}`,
      title,
      readingTimeMin: 3,
      blocks: tmpl.gen(book.title, book.author),
    })
  }

  if (newChapters.length < 10) {
    // Fill remaining with generic
    while (newChapters.length < 10) {
      const idx = newChapters.length
      newChapters.push({
        id: `ch-${s}-${idx + 1}`,
        title: `第${numLabels[idx]}章 · 延伸思考`,
        readingTimeMin: 3,
        blocks: [
          h('更深层的追问'),
          p(`读完《${book.title}》的前面章节后，值得停下来反思：${book.author}的核心观点在你的个人经验中是否得到了验证？有哪些地方与你的直觉相矛盾？`),
          ki('阅读的最高境界不是理解作者说了什么，而是在作者的启发下形成你自己的想法。被动吸收和主动思考之间的差距，就是"读过"和"读懂"之间的差距。'),
          co(`带着这些问题重新翻开《${book.title}》，你会发现一些第一次阅读时没有注意到的细节。每一次重读都是一次新的对话。`),
        ],
      })
    }
  }

  const totalTime = newChapters.reduce((s, c) => s + (c.readingTimeMin || 3), 0)
  const json = JSON.stringify(newChapters).replace(/'/g, "''")

  try {
    await query(`UPDATE BookSummary SET chaptersJson = '${json}', readingTime = ${totalTime} WHERE bookId = '${book.id}'`)
    updated++
    console.log(`✓ ${book.title}: ${existing.length}章 → ${newChapters.length}章`)
  } catch (e) {
    errors++
    console.error(`✗ ${book.title}: ${e.message?.substring(0, 80)}`)
  }
}

console.log(`\n完成: ${updated} 更新, ${errors} 错误`)

