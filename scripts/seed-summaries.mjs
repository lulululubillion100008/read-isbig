/**
 * Seeds book summaries directly into the database.
 * Run: node scripts/seed-summaries.mjs
 */
import { query } from './db-utils.mjs'

function genId() {
  return 'bs-' + Date.now().toString(36) + Math.random().toString(36).substring(2, 8)
}

async function insertSummary(bookTitle, chapters) {
  const book = await query(`SELECT id FROM Book WHERE title = '${bookTitle.replace(/'/g, "''")}'`)
  if (!book.rows.length) { console.error(`找不到: ${bookTitle}`); return false }
  const bookId = book.rows[0].id

  // Check if already has summary
  const existing = await query(`SELECT id FROM BookSummary WHERE bookId = '${bookId}'`)
  if (existing.rows.length) { console.log(`跳过(已有): ${bookTitle}`); return true }

  const totalTime = chapters.reduce((s, c) => s + (c.readingTimeMin || 3), 0)
  const chaptersJson = JSON.stringify(chapters).replace(/'/g, "''")
  const id = genId()

  await query(
    `INSERT INTO BookSummary (id, bookId, pagesJson, themeJson, chaptersJson, contentType, readingTime, generatedAt)
     VALUES ('${id}', '${bookId}', '[]', '{}', '${chaptersJson}', 'deep-analysis', ${totalTime}, datetime('now'))`
  )
  return true
}

