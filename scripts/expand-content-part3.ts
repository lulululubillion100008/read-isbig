async function expandBook(expansion: BookExpansion): Promise<void> {
  const { bookId, title, newChapters, additionalReadingTime } = expansion;

  // 1. 查找现有 BookSummary
  const existing = await prisma.bookSummary.findFirst({
    where: { bookId },
  });

  if (!existing) {
    console.error(`  [SKIP] ${title}: BookSummary not found for ${bookId}`);
    return;
  }

  // 2. 解析现有 chaptersJson
  const existingChapters: Chapter[] = existing.chaptersJson
    ? JSON.parse(existing.chaptersJson)
    : [];

  console.log(`  Found ${existingChapters.length} existing chapters`);

  // 3. 检查是否已经有新章节（防止重复运行）
  const newChapterIds = new Set(newChapters.map((ch) => ch.id));
  const alreadyExists = existingChapters.some((ch) => newChapterIds.has(ch.id));

  if (alreadyExists) {
    console.log(`  [SKIP] ${title}: new chapters already exist, skipping`);
    return;
  }

  // 4. 追加新章节（不可变操作）
  const updatedChapters = [...existingChapters, ...newChapters];
  const updatedReadingTime = existing.readingTime + additionalReadingTime;

  // 5. 更新数据库
  await prisma.bookSummary.update({
    where: { id: existing.id },
    data: {
      chaptersJson: JSON.stringify(updatedChapters),
      readingTime: updatedReadingTime,
    },
  });

  console.log(
    `  [OK] ${title}: ${existingChapters.length} → ${updatedChapters.length} chapters, readingTime → ${updatedReadingTime}min`
  );
}

async function main(): Promise<void> {
  console.log('=== Expand Content Part 3: Appending new chapters ===\n');

  for (const expansion of expansions) {
    console.log(`Processing: ${expansion.title} (${expansion.bookId})`);
    try {
      await expandBook(expansion);
    } catch (error) {
      console.error(`  [ERROR] ${expansion.title}:`, error);
    }
    console.log('');
  }

  console.log('=== Done ===');
}

main()
  .catch((error) => {
    console.error('Fatal error:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
