// ─── Main ───────────────────────────────────────────────────

async function main() {
  console.log('=== 扩充书籍章节内容 (Part 1) ===\n');

  for (const expansion of expansions) {
    const { bookId, bookName, newChapters, additionalMinutes } = expansion;

    // 1. Find existing BookSummary
    const existing = await prisma.bookSummary.findFirst({
      where: { bookId },
    });

    if (!existing) {
      console.log(`✗ ${bookName}: 未找到 BookSummary，跳过`);
      continue;
    }

    // 2. Parse existing chapters
    const existingChapters: Chapter[] = existing.chaptersJson
      ? JSON.parse(existing.chaptersJson)
      : [];

    console.log(`  ${bookName}: 已有 ${existingChapters.length} 章`);

    // 3. Check for duplicate chapter IDs to avoid re-appending
    const existingIds = new Set(existingChapters.map((ch) => ch.id));
    const chaptersToAdd = newChapters.filter((ch) => !existingIds.has(ch.id));

    if (chaptersToAdd.length === 0) {
      console.log(`  ${bookName}: 新章节已存在，跳过`);
      continue;
    }

    // 4. Append new chapters (immutable — create new array)
    const updatedChapters = [...existingChapters, ...chaptersToAdd];
    const updatedReadingTime = existing.readingTime + additionalMinutes;

    // 5. Update the BookSummary
    await prisma.bookSummary.update({
      where: { id: existing.id },
      data: {
        chaptersJson: JSON.stringify(updatedChapters),
        readingTime: updatedReadingTime,
      },
    });

    console.log(
      `✓ ${bookName}: 追加 ${chaptersToAdd.length} 章 → 共 ${updatedChapters.length} 章, 阅读时间 ${updatedReadingTime} 分钟`,
    );
  }

  console.log('\n=== 全部完成 ===');
  await prisma.$disconnect();
}

main().catch((e) => {
  console.error('脚本执行失败:', e);
  prisma.$disconnect();
  process.exit(1);
});
