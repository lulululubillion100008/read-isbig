// For books not in bookChapters, we'll expand by reading existing content
// and generating complementary chapters based on the book's theme

async function expandBook(bookTitle, bookId, existingChapters) {
  const newChapters = bookChapters[bookTitle]
  if (newChapters) {
    // Replace with full 10-chapter version
    const totalTime = newChapters.reduce((s, c) => s + (c.readingTimeMin || 3), 0)
    const chaptersJson = JSON.stringify(newChapters).replace(/'/g, "''")
    await query(
      `UPDATE BookSummary SET chaptersJson = '${chaptersJson}', readingTime = ${totalTime} WHERE bookId = '${bookId}'`
    )
    return newChapters.length
  }
  return existingChapters
}

// Main
const books = await query(`
  SELECT b.id, b.title, b.author, b.category, bs.chaptersJson, bs.id as summaryId
  FROM Book b
  JOIN BookSummary bs ON b.id = bs.bookId
  ORDER BY JSON_ARRAY_LENGTH(bs.chaptersJson) ASC
`)

console.log(`\n处理 ${books.rows.length} 本书...`)

let updated = 0
let skipped = 0
for (const book of books.rows) {
  const existing = JSON.parse(book.chaptersJson)
  if (bookChapters[book.title]) {
    const count = await expandBook(book.title, book.id, existing.length)
    updated++
    console.log(`✓ ${book.title}: ${existing.length}章 → ${count}章`)
  } else {
    skipped++
  }
}

console.log(`\n更新: ${updated}, 待处理: ${skipped}`)
